
package Repeticao;

public class ForEach {
    
    public static void main(String[] args) {
        
        //criação de um vetor simples para demonstração
        int[] v = new int[10]; //como não passei nenhum valor, vai ser um vetor de 10 posições com valores iguais a 0
        
        for(int i:v){
            System.out.print(i + " ");
        }
        
    }
    
}
